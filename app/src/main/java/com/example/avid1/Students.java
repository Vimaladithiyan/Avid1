
    package com.example.avid1;

    public class Students {
        private String name;
        private String rank;

        public Students() {
        }

        public Students(String name, String rank) {
            this.name = name;
            this.rank = rank;
        }

        public String getName() {
            return name;
        }

        public String getRank() {
            return rank;
        }
    }


